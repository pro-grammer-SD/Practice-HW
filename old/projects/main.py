# Set Symmetric Difference

# Lesson 24: Sets and Arrays
# Write a Python program to find the symmetric difference between two sets.

Set1_1 = {'blue', 'green'}
Set2_1 = {'blue', 'yellow'}

Set1_2 = {1, 2, 3, 4, 5}
Set2_2 = {1, 5, 6, 7, 8, 9}

print(Set1_1.symmetric_difference(Set2_1))
print(Set1_2.symmetric_difference(Set2_2))